package estruturadowhile;

public class Ex051 {
    public static void main(String[] args) {

        //51) Faça um programa que exiba os números de 1 a 10 usando o loop "do-while".

        int cont = 1;

        do {
            System.out.print(cont + " ");
            cont++;
        }while (cont <= 10);
    }
}
